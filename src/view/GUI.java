    private void deleteDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteDataActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "DELETE FROM ticket WHERE ID_Order ='" + idorder.getText() + "'";
            java.sql.Connection conn = (Connection) Database.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "DELETE SUCESSFULY...");
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        Show_data();
        Empty_form();
    }//GEN-LAST:event_deleteDataActionPerformed

